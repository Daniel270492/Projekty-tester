
# Python Loops
# For loop

# zaczynamy od słówka for, kolejno dajemy tak zwany iterator. Potem słowo kluczowe "in"
# range(x), określa do kiedy pętla ma się wykonywać
for i in range(5):
    # Wydrukowanie obecnej wartości "i" na konsolę
    print(i)

# While loop

# utworzenie zmiennej
j = 0
# słowo kluczowe while, rozpoczynające pętle, potem warunek logiczny
while j < 5:
    # wyświetlenie zmiennej na konsole
    print(j)
    # j = j + 1
    # powiększenie wartości j o 1
    j += 1



