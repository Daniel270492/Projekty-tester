
// Java Primitive Data Types
public class primitives {
    public static void main(String[] args) {
        String name = "John Doe"; // String is not technically primitive but commonly used
        int age = 30; // int
        boolean isSubscribed = false; // boolean
        // Java doesn't have a direct equivalent for null or undefined as a 'type', but null can be used as a value for non-primitive (Object) types

        System.out.println(name); // Displays the name
        System.out.println(age); // Displays the age
        System.out.println(isSubscribed); // Displays the boolean value
    }
}
