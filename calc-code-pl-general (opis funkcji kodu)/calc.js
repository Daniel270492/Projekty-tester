// JavaScript Simple Calculator
console.log("JavaScript Calculator");

function addJs(a, b) { return a + b; }
// Funkcja dodawania dwóch liczb

function subtractJs(a, b) { return a - b; }
// Funkcja odejmowania jednej liczby od drugiej

function multiplyJs(a, b) { return a * b; }
// Funkcja mnoenia dwóch liczb

function divideJs(a, b) { return a / b; }
// Funkcja dzielenia jednej liczby przez drugą

// Wyświetlanie wyników operacji matematycznych
console.log(addJs(10, 5));
console.log(subtractJs(10, 5));
console.log(multiplyJs(10, 5));
console.log(divideJs(10, 5));