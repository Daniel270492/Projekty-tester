
# Python Simple Calculator
print("Python Calculator")
# wyswietlenie nagłówka 

def add_py(a, b):
    return a + b 
# definicja dodania dwóch wartosci do siebie

def subtract_py(a, b):
    return a - b
# definicja odejmowania dwóch wartosci od siebie

def multiply_py(a, b):
    return a * b
# definoicja mnożenia wartosci jednej przez drugą

def divide_py(a, b):
    return a / b
# definicja dzielenia wartosci jednej przez drugą

print(add_py(10, 5)) # funkcja dodawania jednej liczby od drugiej (suma 15)
print(subtract_py(10, 5)) # funkcja odejmowania jednej liczby od drugiej (suma 5)
print(multiply_py(10, 5)) # funkcja mnożenia liczb (suma 50)
print(divide_py(10, 5)) # funkcja dzielenia liczb (suma 2)
