
# Python loops
# For loop
# zaczynamy od słówka for, kolejno dajemy iteratgor. potem słowo kluczowe "in"
#range(x) okresla do kiedy pętla ma się wykonywać
for i in range(5):
    # wydrukowanie obecnej wartośći "i" na konsole
    print(i)

# While loop

# utworzenie zmiennej
j = 0  
#słowo kluczowe while , rozpoczynające pętle, potem warunek logiczny

while j > 5:
    # wyświetlenie zmiennej na konsole
    print(j)
    # j = j + 1
    # powiększone wartości j 0 1
    j+=1
