
// Java Loops
public class loops {
    public static void main(String[] args) {
        // For loop
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }

        // While loop
        int j = 0;
        while (j < 5) {
            System.out.println(j);
            j++;
        }
    }
}
